#include <stdio.h>

int main()
{
    int x = 1;                   // integer
    float y = 6.9;               // floating point
    char z = 'a';                // character
    char str[] = "Hello World!"; // string
    printf("%d\n", x);
    printf("%f\n", y);
    printf("%c\n", z);
    printf("%s\n", str);

    return 0;
}